  _onDrag(e) {
    if (!this._dragging) return;
    const { type, propIdx } = this._dragging;
    const prop = this._proposals[propIdx];

    // Convertir deplacement pixels en metres locaux
    let dxM, dyM;
    if (this._map) {
      // Coordonnees RELATIVES au canvas Mapbox (map.unproject attend ca)
      const rect = this._canvas.getBoundingClientRect();
      const prevX = this._dragging.prevX ?? (this._dragging.startX - rect.left);
      const prevY = this._dragging.prevY ?? (this._dragging.startY - rect.top);
      const currX = e.clientX - rect.left;
      const currY = e.clientY - rect.top;

      const geo1 = this._map.unproject([prevX, prevY]);
      const geo2 = this._map.unproject([currX, currY]);

      const c = this._centroidGeo(this._parcelGeo);
      const LNG = 111320 * Math.cos(c[1] * Math.PI / 180);
      const LAT = 111320;
      dxM =  (geo2.lng - geo1.lng) * LNG;
      dyM = -(geo2.lat - geo1.lat) * LAT;

      this._dragging.prevX = currX;
      this._dragging.prevY = currY;
    } else {
      let rawDx = (e.clientX - this._dragging.startX) / this.SCALE;
      let rawDy = (e.clientY - this._dragging.startY) / this.SCALE;
      // Compenser la rotation de la vue standalone
      const bearing = this._parcelBearing || 0;
      if (Math.abs(bearing) > 1) {
        const rad = bearing * Math.PI / 180;
        const cos = Math.cos(rad), sin = Math.sin(rad);
        dxM =  cos * rawDx + sin * rawDy;
        dyM = -sin * rawDx + cos * rawDy;
      } else {
        dxM = rawDx;
        dyM = rawDy;
      }
      this._dragging.startX = e.clientX;
      this._dragging.startY = e.clientY;
    }

    const SNAP = 0.5;
    const engine = window.PlanMasseEngine;

    // ── AABB Body drag (translate) ────────────────────────────
    if (type === 'body' && this._buildingAABB) {
      const b = this._buildingAABB, b0 = this._dragging.bat0;
      b.x = engine?.snap(b0.x + dxM) ?? Math.round((b0.x + dxM) / SNAP) * SNAP;
      b.y = engine?.snap(b0.y + dyM) ?? Math.round((b0.y + dyM) / SNAP) * SNAP;
      // Accumuler le delta cumulé
      this._dragging.bat0 = { ...b0, x: b0.x + dxM, y: b0.y + dyM };
      this._dragging.bat0.x = b.x; this._dragging.bat0.y = b.y;
      // Clamp dans enveloppe
      if (engine && this._engineTerrain) {
        engine.clampBat(b, engine.computeEnv(this._engineTerrain));
      }
      // Sync polygon depuis AABB
      this._syncPolygonFromAABB(prop);
      this._renderProposal(propIdx);
      this._renderScorePanel();
      return;
    }

    // ── AABB Handle drag (resize 8 directions) ────────────────
    if (type === 'handle' && this._buildingAABB) {
      const b = this._buildingAABB, b0 = this._dragging.bat0, h = this._dragging.handle;
      const snap = v => engine?.snap(v) ?? Math.round(v / SNAP) * SNAP;
      const MIN_W = engine?.MIN_W ?? 4, MIN_L = engine?.MIN_L ?? 5;

      // Ouest (sw, w, nw) : déplacer x, ajuster w
      if (h === 'sw' || h === 'w' || h === 'nw') {
        const nx = snap(b0.x + dxM);
        const nw = b0.x + b0.w - nx;
        if (nw >= MIN_W) { b.x = nx; b.w = nw; }
      }
      // Est (se, e, ne) : ajuster w
      if (h === 'se' || h === 'e' || h === 'ne') {
        const nw = snap(b0.w + dxM);
        if (nw >= MIN_W) b.w = nw;
      }
      // Sud (sw, s, se) : déplacer y, ajuster l
      if (h === 'sw' || h === 's' || h === 'se') {
        const ny = snap(b0.y + dyM);
        const nl = b0.y + b0.l - ny;
        if (nl >= MIN_L) { b.y = ny; b.l = nl; }
      }
      // Nord (nw, n, ne) : ajuster l
      if (h === 'nw' || h === 'n' || h === 'ne') {
        const nl = snap(b0.l + dyM);
        if (nl >= MIN_L) b.l = nl;
      }

      if (engine && this._engineTerrain) {
        engine.clampBat(b, engine.computeEnv(this._engineTerrain));
      }
      this._syncPolygonFromAABB(prop);
      this._renderProposal(propIdx);
      this._renderScorePanel();
      return;
    }

    // ── Polygon node drag ─────────────────────────────────────
    const GRID = 1;
    if (type === 'node') {
      const { nodeIdx } = this._dragging;
      prop.polygon[nodeIdx].x = Math.round((prop.polygon[nodeIdx].x + dxM) / GRID) * GRID;
      prop.polygon[nodeIdx].y = Math.round((prop.polygon[nodeIdx].y + dyM) / GRID) * GRID;
    } else if (type === 'edge') {
      const { edgeIdx } = this._dragging;
      const j = (edgeIdx + 1) % prop.polygon.length;
      const p1 = prop.polygon[edgeIdx], p2 = prop.polygon[j];
      p1.x = Math.round((p1.x + dxM) / GRID) * GRID;
      p1.y = Math.round((p1.y + dyM) / GRID) * GRID;
      p2.x = Math.round((p2.x + dxM) / GRID) * GRID;
      p2.y = Math.round((p2.y + dyM) / GRID) * GRID;
    }

    // Mettre a jour polygonGeo
    prop.polygonGeo = this._localToGeo(prop.polygon);
    prop.surface = this._polygonAreaLocal(prop.polygon);

    // Recalculer le score
    window.EnvelopeGenerator._scorePareto && (() => {
      const sd = prop.scoreData;
      if (sd) {
        const parcelArea = parseFloat(this._session?.terrain?.contenance_m2 ?? 200);
        const ces = parcelArea > 0 ? prop.surface / parcelArea : 0;
        sd.pluScore = ces <= (parseFloat(this._session?.phases?.[4]?.data?.ces_max ?? 0.7)) ? 1 : 0.5;
        sd.gardenScore = Math.max(0, Math.min(1, (1 - prop.surface / parcelArea) * 1.5));
        prop.score = window.EnvelopeGenerator._aggregateScore(sd);
      }
    })();

    this._renderProposal(propIdx);
    this._renderScorePanel();
  },

