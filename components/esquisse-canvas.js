    const env = this._engineTerrain ? engine?.computeEnv(this._engineTerrain) : null;

    // Largeur bâtiment (bas)
    this._dimLine(bat.x, bat.y, bat.x + bat.w, bat.y, `${bat.w.toFixed(1)} m`, -2.2, 'h', '#374151', g);
